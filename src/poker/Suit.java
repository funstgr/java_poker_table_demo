package poker;

/**
 * Enum class used to create ordinal value card suits. This enum class is based
 * on the ordinal values of standard Bridge suit low to high order. If an
 * additional ordering was required an integer based constructor with integer
 * values can be added.
 * 
 * @author Gregory L.Funston
 * @version 1.0
 */

public enum Suit {
	Clubs, Diamonds, Hearts, Spades
}
